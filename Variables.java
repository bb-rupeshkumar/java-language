public class Variables {
    public static void main(String[] args){
        int a = 10;
        long l = 100l;
        double d = 10.5;
        boolean b = true;
        char ch = 'A';

        System.out.println(a);
        System.out.println(l);
        System.out.println(d);
        System.out.println(b);
        System.out.println(ch);
    }
}